// 虽然这里只定义了4ms,但是需要注意
// 宏任务依旧需要等待主线程任务做完成才能执行
// 所以这里的顺序,依然不变
// 当然4ms还是与主程序运行的时刻开始的

setTimeout(() => {
  console.log('定时器1')
}, 4);

// 当有两个定时器时,若时间一样,就会按照顺序输出
// 当有两个定时器时,若时间不一样,则按照时间有小到大输出
setTimeout(() => {
  console.log('定时器2')
}, 4);


